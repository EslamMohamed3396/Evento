package com.example.eslam.wheretogo.Model;

public class Category_Model {
    private String mName;
    private int mImage;

    public Category_Model(String mName, int mImage) {
        this.mName = mName;
        this.mImage = mImage;
    }

    public String getmName() {
        return mName;
    }

    public int getmImage() {
        return mImage;
    }
}

